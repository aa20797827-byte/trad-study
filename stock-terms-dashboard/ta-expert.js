// ── SVG helpers ──────────────────────────────────────────────────────────────
// ViewBox 0 0 240 130, y=0 top (high price), y=130 bottom (low price)
function _C(x,hy,oy,cy,ly,col){
  const bt=Math.min(oy,cy),bb=Math.max(oy,cy),bh=Math.max(bb-bt,2);
  return `<line x1="${x}" y1="${hy}" x2="${x}" y2="${bt}" stroke="${col}" stroke-width="1.5"/>
<rect x="${x-5}" y="${bt}" width="10" height="${bh}" fill="${col}" rx="0.5"/>
<line x1="${x}" y1="${bb}" x2="${x}" y2="${ly}" stroke="${col}" stroke-width="1.5"/>`;
}
const _BU=(x,hy,oy,cy,ly)=>_C(x,hy,oy,cy,ly,'#22c55e'); // bullish: cy<oy
const _BE=(x,hy,oy,cy,ly)=>_C(x,hy,oy,cy,ly,'#ef4444'); // bearish: oy<cy

function _grid(){
  return `<rect width="240" height="130" fill="#0a0f1e"/>
<line x1="0" y1="43" x2="240" y2="43" stroke="rgba(255,255,255,0.05)" stroke-width="0.5"/>
<line x1="0" y1="86" x2="240" y2="86" stroke="rgba(255,255,255,0.05)" stroke-width="0.5"/>`;
}

function _txt(x,y,s,col,size){
  return `<text x="${x}" y="${y}" fill="${col||'rgba(255,255,255,0.65)'}" font-size="${size||7}" font-family="sans-serif">${s}</text>`;
}

function _line(x1,y1,x2,y2,col,dash){
  return `<line x1="${x1}" y1="${y1}" x2="${x2}" y2="${y2}" stroke="${col||'rgba(255,255,255,0.4)'}" stroke-width="1" stroke-dasharray="${dash||'none'}"/>`;
}

function _fig(svgBody,caption){
  return `<figure class="ta-fig">
  <svg viewBox="0 0 240 130" xmlns="http://www.w3.org/2000/svg">${_grid()}${svgBody}</svg>
  <figcaption>${caption}</figcaption>
</figure>`;
}

// ── Tab switcher ──────────────────────────────────────────────────────────────
function tabTA(id){
  document.querySelectorAll('.ta-tab-btn').forEach(b=>b.classList.toggle('on',b.dataset.tab===id));
  document.querySelectorAll('.ta-tab-pane').forEach(p=>p.style.display=p.id===id?'block':'none');
}

// ── Section builder ───────────────────────────────────────────────────────────
function _sec(icon,title,desc,figs,rules,warns){
  const rulesHtml=rules?`<div class="ta-expert-rules"><div class="ta-rules-title">📋 매매 규칙</div>${rules.map(r=>`<div class="ta-rule-item"><span class="ta-rule-dot"></span>${r}</div>`).join('')}</div>`:'';
  const warnHtml=warns?`<div class="ta-expert-warn">${warns.map(w=>`<div>⚠ ${w}</div>`).join('')}</div>`:'';
  return `<div class="ta-expert-sec">
  <div class="ta-expert-header"><span class="ta-expert-icon">${icon}</span><h3 class="ta-expert-title">${title}</h3></div>
  <p class="ta-expert-desc">${desc}</p>
  <div class="ta-figs">${figs}</div>
  ${rulesHtml}${warnHtml}
</div>`;
}

